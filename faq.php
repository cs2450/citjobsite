<h1>Frequently Asked Questions</h1>
<div class="question">
	<h2>Q. What information is sent when I apply for a job?</h2>
	<div>The information on your profile page gets sent exactly as you yourself see it. It is not a link to your profile, it is a snapshot of your current profile sent in an HTML email. The only differences are that the edit buttons and the matches column are removed, and a link to your resume, if any, is added.
	</div>
	<br/>
	<h2>Q. How does the matching work?</h2>
	<div>The built-in matching system is a useful feature for helping you find the jobs suited for you. It is completely optional and will NOT hinder normal job searching in any way. Job seekers can select the skills they have from the list and their matches will be based off of them. Employers have the option to select the skills they are looking for when they post jobs. They have three options when they select the skills they want. Any checked skills will show up in the job listing regardless of what option is selected.</div>
	<ul>
		<li>Selecting "None" will display the skill in the job posting, but it will not count for matching.</li>
		<li>Selecting "Required" is used for skills that are absolutely necessary to fill the job. Using this selection will cause the job to not appear on seeker's Match List if they do not have the required skill.<br />*Please note that this ONLY effects matching. The job will appear as normal in the Jobs List*</li>
		<li>Selecting "Desirable" will cause the skill to be compared with the seeker's skills. The two lists are compared and will be ranked by percentage. After all the active jobs have been compared, the seeker is presented with their matches in ranked order.</li>
	</ul>
</div>
